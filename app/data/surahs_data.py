def get_surah_data_for_page(
    page: int, items_per_page: int = 10
) -> list[dict[str, Any]]:
    """
    Get surah data for a specific page.

    Args:
        page: Page number (0-based)
        items_per_page: Number of items per page

    Returns:
        List of surah data for the requested page
    """
    start_idx = page * items_per_page
    end_idx = start_idx + items_per_page
    return COMPLETE_SURAHS_DATA[start_idx:end_idx]


def get_total_pages(items_per_page: int = 10) -> int:
    """
    Get total number of pages needed for all surahs.

    Args:
        items_per_page: Number of items per page

    Returns:
        Total number of pages
    """
    return (len(COMPLETE_SURAHS_DATA) + items_per_page - 1) // items_per_page


def get_surah_by_number(surah_number: int) -> dict[str, Any]:
    """
    Get surah data by number.

    Args:
        surah_number: Surah number (1-114)

    Returns:
        Surah data dictionary
    """
    for surah in COMPLETE_SURAHS_DATA:
        if surah["number"] == surah_number:
            return surah
    return {}


def search_surahs(query: str) -> list[dict[str, Any]]:
    """
    Search surahs by English or Arabic name.

    Args:
        query: Search query

    Returns:
        List of matching surahs
    """
    query_lower = query.lower()
    results = []

    for surah in COMPLETE_SURAHS_DATA:
        if (
            query_lower in surah["name_english"].lower()
            or query_lower in surah["name_arabic"]
            or str(surah["number"]) == query_lower
        ):
            results.append(surah)

    return results
